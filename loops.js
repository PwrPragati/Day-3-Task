// For Loop
for (let i = 1; i <= 5; i++) {
    console.log(`Iteration ${i}`);
}

// While Loop
let count = 1;
while (count <= 3) {
    console.log(`Count: ${count}`);
    count++;
}

// Do...While Loop
let num2 = 1;
do {
    console.log(`Number: ${num2}`);
    num2++;
} while (num2 <= 3);
